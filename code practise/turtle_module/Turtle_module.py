from turtle import Turtle,Screen
timmy=Turtle()
timmy.shape("turtle")#it will defined the shape
timmy.color("blue")#it allocate the colour of the turtle
timmy.forward(100)#it will move forward from the present position of turtle

timmy.circle(50)#it will print the circle.
timmy.dot(25)#it will print the dot at the starting of the turtle object.
timmy.back(200)#it will back the postion of turtle from the present position of the turtle
timmy.circle(50)
timmy.dot(25)
timmy.right(90)


timmy.clear(50)#clear is used for exit the screen after the code will successfully exicuted.
#print(timmy)


screen=Screen()
screen.exitonclick()
